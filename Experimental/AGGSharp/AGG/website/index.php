    <p>

    Agg-Sharp is a C# port of the Anti-Grain Geometry library
	- AGG was originally written by Maxim Shemanarev in C++.  Agg-Sharp was inspired by Maxim's wonderful work on AGG and further by the Pascal Port of agg, <a href="http://aggpas.org/">aggpass</a>.
	Agg-Sharp is Open Source and free of charge.<p>
 
    Agg-Sharp does not depend on any graphic API or technology. You can think 
    of Agg-Sharp (and all the AGGs) as a rendering engine that produces pixel images in memory from 
    vectorial data. But of course, Agg-Sharp can do much more than that. The ideas and 
    the philosophy of Agg-Sharp are tha same as those of AGG and you can can read more about those on AGGs website <a href="http://www.antigrain.com/about">here</a> ...
    </p>
